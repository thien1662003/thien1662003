package thanhthien_cntt1;
public class Point2D{
	public float x;
	public float y;
	
	public Point2D() {
		this.x = 0;
		this.y = 0;
	}
	public float Point2D(float x, float y)
	{
		this.x = x;
		this.y = y;
		return 0;
	}
	public float getx() {
		return x;
	}
	public float gety() {
		return y;
	}
}